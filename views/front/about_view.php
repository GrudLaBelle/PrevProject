    <h2>Qui sommes-nous?</h2>
    <p>PrevPro est une association qui propose aux entreprises un outil un suivi de la prévention des risques professionnels simple et facile d'accès. C'est un service gratuit en ligne destiné à améliorer la prévention des risques professionnels dans tous les secteurs d'activité. Cet outil permet de comparer les données de sinistralité de son entreprise avec les données nationales de sinistralité correspondant précisément secteur d'activité concerné grâce au code APE (Activité Principale Exercée).</p>
